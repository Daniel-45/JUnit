package com.dam.parametrizados;

/**
 * 
 * @author Daniel
 *
 */
public class ParametrizadosNumeros {

	/**
	 * Devuelve la suma de los números pasados como parámetros
	 * 
	 * @param a
	 * @param b
	 * @return a + b
	 */
	public int suma(int a, int b) {
		return a + b;
	}

	/**
	 * Devuelve la resta de los números pasados como parámetros
	 * 
	 * @param a
	 * @param b
	 * @return a - b
	 */
	public int resta(int a, int b) {
		return a - b;
	}

	/**
	 * Devuelve la división de los números pasados como parámetros
	 * 
	 * @param a
	 * @param b
	 * @return a / b
	 */
	public int divison(int a, int b) {
		return a / b;
	}

	/**
	 * Devuelve la multiplicación de los números pasados como parámetros
	 * 
	 * @param a
	 * @param b
	 * @return a / b
	 */
	public int producto(int a, int b) {
		return a * b;
	}

}

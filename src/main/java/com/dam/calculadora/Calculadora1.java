public class Calculadora1 {

	// Suma
	public static int suma(int a, int b) {
		return a + b;
	}

	// Resta
	public static int resta(int a, int b) {
		return a - b;
	}

	// Multiplicación
	public static int multiplicacion(int a, int b) {
		return a * b;
	}

	// División
	public static int division(int a, int b) {
		return a / b;
	}
}
